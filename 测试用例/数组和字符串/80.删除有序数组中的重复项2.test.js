const removeDuplicates = require("../../数组和字符串/80.删除有序数组中的重复项2");
removeDuplicates([2, 3, 3]); //3, nums = [2,3,3]
removeDuplicates([1, 1, 1, 2]); //3, nums = [1,1,2]
removeDuplicates([1, 1, 1, 2, 2, 3]); //5, nums = [1,1,2,2,3]
removeDuplicates([0, 0, 1, 1, 1, 1, 2, 3, 3]); //7, nums = [0,0,1,1,2,3,3]
removeDuplicates([1, 1, 1, 1, 2, 3, 3]); //[1,1,2,3,3]
